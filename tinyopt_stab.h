#ifndef TINYOPT_STAB
#define TINYOPT_STAB

#include "tinyopt_ast.h"

#define SYMBOL_TABLE_SIZE 211
#define GLOBAL_TABLE_SIZE 97

typedef enum
{
  VALUE_TYPE_UNKNOWN,
  VALUE_TYPE_INT,
  VALUE_TYPE_STRING,
} ValueType;

typedef struct
{
  char *str;
  int number;
  ValueType type;
} SymbolValue;

typedef struct Symbol
{
  TinyOptASTNode_t *node;
  char *name;
  SymbolValue *value;
  uint64_t loop_hash;
  uint64_t id_hash;
} Symbol;

typedef struct SymbolBucket
{
  Symbol *symbol;
  struct SymbolBucket *next;
} SymbolBucket;

typedef struct symbol_table TinyOptStab_t;

struct symbol_table
{
  SymbolBucket **buckets;
  size_t size;
};

uint64_t stab_hash_string (const char *str);
TinyOptStab_t *stab_create (size_t size);
Symbol *stab_get (TinyOptStab_t *table, const char *scope, const char *name);
void stab_insert (TinyOptStab_t *table, Symbol *symbol, const char *scope);
void tinyopt_stab_init (TinyOptASTNode_t *node, TinyOptStab_t *stab,
                        uint64_t loop_hash, const char *scope);
void free_stab (TinyOptStab_t *table);
SymbolValue *resolve_final_value (TinyOptStab_t *stab, const char *name,
                                  const char *scope);
SymbolValue *evaluate_expression_value (TinyOptASTNode_t *expr,
                                        TinyOptStab_t *table, char *scope);

#endif
