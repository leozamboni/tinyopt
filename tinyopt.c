#include "tinyopt.h"
#include "tinyopt_ast.h"
#include "tinyopt_cfg.h"
#include "tinyopt_code.h"
#include "tinyopt_core.h"
#include <stdio.h>

int yyparse (TinyOptASTNode_t *global_ast);

int
main (int argc, char **argv)
{

  TinyOpt_t *tinyopt;
  tinyopt_init (&tinyopt);
  tinyopt_flags (&tinyopt, argc, argv);
  tinyopt_compile (&tinyopt);
  tinyopt_free (&tinyopt);
  return EXIT_SUCCESS;
}

void
tinyopt_init (TinyOpt_t **tinyopt)
{
  (*tinyopt) = malloc (sizeof (TinyOpt_t));
  (*tinyopt)->stab = stab_create (SYMBOL_TABLE_SIZE);
  (*tinyopt)->flags = malloc (sizeof (TinyOptFlags_t));
  (*tinyopt)->ast = create_program_node ();
}

void
tinyopt_flags (TinyOpt_t **tinyopt, int argc, char **argv)
{
  for (int i = 1; i < argc; i++)
    {
      if (strcmp (argv[i], "--cfg") == 0)
        {
          (*tinyopt)->flags->emit_cfg = 1;
        }
    }
}

void
tinyopt_compile (TinyOpt_t **tinyopt)
{
  if (!(*tinyopt)->ast)
    return;

  yyparse ((*tinyopt)->ast);
  tinyopt_optimize (&(*tinyopt));

  if ((*tinyopt)->flags->emit_cfg)
    {
      print_cfg_dot ((*tinyopt)->ast);
      return;
    }

  print_optimized_code ((*tinyopt)->ast);
}

void
tinyopt_free (TinyOpt_t **tinyopt)
{
  if ((*tinyopt)->ast)
    {
      free_ast ((*tinyopt)->ast);
    }

  free_stab ((*tinyopt)->stab);

  free ((*tinyopt)->flags);
  free ((*tinyopt)->stab);
  free ((*tinyopt));
}